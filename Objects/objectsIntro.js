// In JS, "object" usually refers to a type of key-value structure (like a Python dictionary).
// Example:

const person = {
	name: "Maxine",
	age: 32,
	address: "London, E1 123",
};

// 2 ways to access an object's property:
console.log(person.name); // RESULT: Maxine
console.log(person["name"]); // RESULT: Maxine

// More for repetition:
console.log(person.age);
console.log(person["age"]);

console.log(person.address);
console.log(person["address"]);
